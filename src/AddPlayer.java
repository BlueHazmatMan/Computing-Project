    private void createPlayerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createPlayerActionPerformed
        // TODO add your handling code here:
        DatabaseInteractions dbi = new DatabaseInteractions();
        TempPlayerInteractions tpi = new TempPlayerInteractions();
        Player player = getPlayerInfo();
        if (!player.getName().isEmpty()) {
            if (!dbi.getPlayerList().contains(player.getName())) {
                dbi.addPlayer(player);
                tpi.addPlayerToTemp(getPlayerInfo().getName());
                PlayerSettings ps = new PlayerSettings();
                ps.setLocationRelativeTo(null);
                ps.setVisible(true);
                this.dispose();
            } else {
                jDialog1.setLocationRelativeTo(null);
                jDialog1.setSize(360, 200);
                jDialog1.setVisible(true);
            }
        }
    }//GEN-LAST:event_createPlayerActionPerformed


    private Player getPlayerInfo() {
        Player newPlayer = new Player();
        newPlayer.setName(PlayerName.getText());
        newPlayer.setLevel(Integer.valueOf(playerLevels.getSelectedItem().toString()));
        newPlayer.setClass(playerClass.getSelectedItem().toString());
        return newPlayer;
    }


public class Furniture {

    private int furnitureID;
    private String furnitureName;
    
    public String getName(){
        return furnitureName;
    }
    
    public void setName(String value){
        furnitureName = value;
    }
    
    public int getID(){
        return furnitureID;
    }
    
    public void setID(int value){
        furnitureID = value;
    }
    
}

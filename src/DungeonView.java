    private void UIPlayerListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UIPlayerListMouseClicked
        // TODO add your handling code here:
        String selectedPlayer = UIPlayerList.getSelectedValue();
        if (!selectedPlayer.isEmpty()) {
            String playerInfo = playerData(selectedPlayer);
            createPopup(playerInfo, selectedPlayer);
        }
    }//GEN-LAST:event_UIPlayerListMouseClicked

    private String playerData(String playerName) {
        DatabaseInteractions dbi = new DatabaseInteractions();
        String info = playerName + ": \n";
        info = info + "Level " + dbi.getPlayerLevel(playerName) + " " + dbi.getPlayerClass(playerName);
        return info;
    }

    private void createPopup(String info, String title) {
        jDialog1.setLocationRelativeTo(null);
        jDialog1.setSize(260, 310);
        jDialog1.setVisible(true);
        jDialog1.setTitle(title);
        roomDetails.setText(info);
    }


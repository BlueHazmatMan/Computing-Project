    private void setupPlayerList() {
        SetupDungeon sd = new SetupDungeon();
        ArrayList<Player> playerList = sd.getPlayerList();
        String[] players = new String[playerList.size()];
        DefaultListModel list = new DefaultListModel();
        for (int i = 0; i < playerList.size(); i++) {
            players[i] = playerList.get(i).getName();
        }
        UIPlayerList.setListData(players);
    }

    private void setupRooms() {
        SetupDungeon sd = new SetupDungeon();
        ArrayList<String> rooms = sd.getRoomList();
        String[] roomArray = new String[rooms.size()];
        DefaultListModel list = new DefaultListModel();
        for (int i = 0; i < rooms.size(); i++) {
            roomArray[i] = "Room " + (i + 1);
        }
        roomList.setListData(roomArray);
    }


    private void roomListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_roomListMouseClicked
        // TODO add your handling code here:
        SetupDungeon sd = new SetupDungeon();
        int selectedRoom = roomList.getSelectedIndex();
        String roomInfo;
        if (selectedRoom >= 0) {
            roomInfo = sd.getRoom(selectedRoom);
            createPopup(roomInfo, "Room" + (selectedRoom + 1));
        }
    }//GEN-LAST:event_roomListMouseClicked

